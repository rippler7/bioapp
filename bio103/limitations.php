<h2>2) Limitations of the Scientific Method and Its Limitations</h2>
<p class="smallWords">
	Nothing is completely finished in science: the search for better theories is endless. However, observation and experimentation limit the application of the scientific method. Certain concepts exist for which the scientific method is inappropriate.

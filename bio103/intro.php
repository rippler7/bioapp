
<h1>A. Introduction</h1>
<h2>1. Plant Biology as a Science</h2>

<p class="smallWords">
	Plant biology is a science, traditionally called Botany. Botany comes from the Latin word <em>"botanicus"</em> and the Greek word <em>“botane”</em> which means plant. This word can be traced back to the Greek word <em>“boskein”</em> which means to graze. It is derived from the Greek word <em>“bous”</em> that means cattle. Thus etymologically, botany means the science of what cattle eat.
</p>
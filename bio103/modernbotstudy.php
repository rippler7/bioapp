<h2>b. Modern Botanical Study</h2>
<div class="smallWords">
<p>
	Botany came into its own as a Science, divorced from superstition and fantasy with the development of Chemistry, Physics and related sciences. Then, it developed slowly, plants were studied for their intrinsic interesting features of structure and behavior.
</p>
<p>Now Plant Biology encompasses all aspects of plant life: from the molecular and structural organization, how energy is acquired, transferred and used; and basis of responses; to plant breeding, reproduction and survival.
</p>
